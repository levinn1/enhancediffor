public class enhancedfor {
    public static void main(String[] args) {
        int[] numbers = {1, 2, 3, 4, 5}; //menyimpan angka dalam array

        for (int number : numbers) {// enhanced for untuk ambil angka dari array, numbers itu angka dalam array yang akan dipakai dan number itu hasilnya
            System.out.println(number); //print hasil kode di terminal
        }
    }
}